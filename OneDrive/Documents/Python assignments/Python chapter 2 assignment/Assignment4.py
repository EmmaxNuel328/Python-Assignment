y = 2
print(x, '+', y, '=',(x + y))

print(x, '-', y, '=',(x - y))
print(x, '*', y, '=',(x * y))
print(x, '/', y, '=',(x / y))
print(x, '//', y,'=',( x // y))
print(x, '**', y, '=',(x ** y))


